    private void showProductsByCategory(Catalog selectedCategory) {
        jPanel2.removeAll();
        jPanel2.setLayout(new BoxLayout(jPanel2, BoxLayout.Y_AXIS));

        List<Product> allProducts = productRepo.getAllProducts();

        for (Product p : allProducts) {
            if (p.getCatalog() == selectedCategory) {
                JButton productButton = new JButton(
                    p.getProductID() + " - " + p.getProductName() + "  " + String.format("%.2f ‡∏ø", p.getPrice())
                );
                productButton.setMaximumSize(new Dimension(499, 40));
                productButton.setAlignmentX(Component.LEFT_ALIGNMENT);
                productButton.setBackground(Color.WHITE);
                productButton.setFocusPainted(false);
                productButton.setBorder(BorderFactory.createLineBorder(Color.LIGHT_GRAY));

                // ‡πÄ‡∏°‡∏∑‡πà‡∏≠‡∏Å‡∏î‡∏õ‡∏∏‡πà‡∏° ‚Üí set ProductID
                productButton.addActionListener(e -> jTextField1.setText(p.getProductID()));

                jPanel2.add(productButton);
                jPanel2.add(Box.createRigidArea(new Dimension(0, 0)));
            }
        }

        jPanel2.revalidate();
        jPanel2.repaint();
    }
